% Multiply x1 and x3 to generate 'xmult'
fs = 8000;
x1 = 150;
x2 = 450;
x3 = 1500;
N = 128;
t = 0 : 1/fs : N/fs;
signal1 = sin(2*pi*x1*t);
subplot(1,3,1);
plot(signal1);
title('signal_1 with frequency 150Hz');
xlabel('time');
ylabel('Ampitude');
signal3 = sin(2*pi*x3*t);
subplot(1,3,2);
plot(signal3);
title('signal_3 with frequency 1500Hz');
xlabel('time');
ylabel('Ampitude');
subplot(1,3,3);
plot(signal1.*signal3);
title('Multiplied signals');
xlabel('time');
ylabel('Ampitude');
